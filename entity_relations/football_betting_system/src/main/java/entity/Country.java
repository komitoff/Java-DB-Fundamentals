package entity;

import javax.persistence.*;

@Entity
@Table(name = "country")
public class Country {
    private Long id;
    private String name;

    public Country() {}

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Column(name = "name")
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
